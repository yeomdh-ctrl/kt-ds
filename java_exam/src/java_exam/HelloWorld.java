package java_exam;

// Class ==> 프로그램의 최소 실행 단위.
//
public class HelloWorld {

	// 메소드: 프로그램이 어떤 방법으로 실행이 될 것인지 작성하는 영역
	// public static void main(String[] args) {...}
	// -> HelloWorld 클래스를 실행시킬 수 있는 메소드.
	public static void main(String[] args) {
		// "Hello, World!" 문장을 출력해보자!
		//출력을 위한 코드: System.out.prinln(출력 문장 혹은 수식);
		System.out.println("Hello, World!");
		System.out.println(123123);
		System.out.println(3 + 3);
		System.out.println(3 / 2); //div 연산 ==> 나눗셈의 몫을 구함 
		System.out.println(3 % 2); //mod 연산 ==> 나눗셈의 나머지를 구함
		// 실행방법: Ctrl + F11

	}

}
